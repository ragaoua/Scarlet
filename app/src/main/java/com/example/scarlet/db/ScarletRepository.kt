package com.example.scarlet.db

class ScarletRepository(
    private val dbInstance: ScarletDatabase
) {

    ///////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////// BLOCK ////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    fun getBlockById(blockId: Int) = dbInstance.blockDao.getBlockById(blockId)
    fun getBlocksByCompleted(completed: Boolean) =
        dbInstance.blockDao.getBlocksByCompleted(completed)

    ///////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////// SESSION ///////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    fun getSessionById(sessionId: Int) = dbInstance.sessionDao.getSessionById(sessionId)
    fun getSessionsByBlockId(blockId: Int) = dbInstance.sessionDao.getSessionsByBlockId(blockId)

    ////////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////// EXERCISE ///////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////
    fun getExercisesBySessionId(sessionId: Int) =
        dbInstance.exerciseDao.getExercisesBySessionId(sessionId)
}